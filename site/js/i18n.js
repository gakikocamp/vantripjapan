// ── Language Switch Logic ──

let currentLang = 'en';

window.switchLang = function(lang) {
    currentLang = lang;

    // Update URL dynamically without full page reload
    const url = new URL(window.location);
    url.searchParams.set('lang', lang);
    window.history.pushState(null, '', url.toString());

    // Update active button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        const text = btn.textContent.trim().toLowerCase();
        btn.classList.toggle('active', text === lang ||
            (lang === 'zh' && text.includes('繁')) ||
            (lang === 'he' && text.includes('he')));
    });

    // Update all translatable elements
    const dict = translations[lang] || translations.en;
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.innerHTML = dict[key];
        }
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dict[key]) {
            el.placeholder = dict[key];
        }
    });

    // Update html lang attribute
    const langMap = { en: 'en', fr: 'fr', de: 'de', zh: 'zh-Hant', he: 'he' };
    document.documentElement.lang = langMap[lang] || 'en';

    // Show Israel license card only for Hebrew
    var ilCard = document.getElementById('license-card-il');
    if (ilCard) ilCard.style.display = (lang === 'he') ? '' : 'none';

    // Save preference
    try { localStorage.setItem('vtj_lang', lang); } catch (e) { }
};

// Handle browser back/forward buttons
window.addEventListener('popstate', () => {
    const params = new URLSearchParams(window.location.search);
    const langParam = params.get('lang');
    if (langParam && translations[langParam]) {
        switchLang(langParam);
    }
});

// Auto-detect language on load
(function () {
    const params = new URLSearchParams(window.location.search);
    const langParam = params.get('lang');
    
    if (langParam && translations[langParam]) {
        switchLang(langParam);
        return;
    }

    try {
        const saved = localStorage.getItem('vtj_lang');
        if (saved && translations[saved]) {
            switchLang(saved);
            return;
        }
    } catch (e) { }

    const browserLang = (navigator.language || '').toLowerCase();
    if (browserLang.startsWith('fr')) switchLang('fr');
    else if (browserLang.startsWith('de')) switchLang('de');
    else if (browserLang.startsWith('zh')) switchLang('zh');
    else if (browserLang.startsWith('he')) switchLang('he');
    else switchLang('en');
})();
